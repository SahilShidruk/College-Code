#include <iostream>

int main() {
    // This program calculates the sum of two integers

    // Declare variables
    int a, b, sum;
    // Take input from user
    printf("Enter value of a: ");
    scanf("%d", &a);
    printf("Enter value of b: ");
    scanf("%d", &b);
    // Calculate sum
    sum = a + b;
    // Display the result
    printf("Sum of a and b is: %d\n", sum);
    return 0;
}